package br.com.dio.model;

/**
 * @author Marcela Oliveira
 *
 */
public class Gato {
	
	public String nome;
	public String cor;
	public Integer idade;
	
	public Gato() {
		super();
	}

	public Gato(String nome, String cor, Integer idade) {
		super();
		this.nome = nome;
		this.cor = cor;
		this.idade = idade;
	}
	
	

}
